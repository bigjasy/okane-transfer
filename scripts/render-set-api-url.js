const fs = require('fs');
const path = require('path');

function resolveApiBaseUrl() {
  const explicit = (process.env.API_BASE_URL || '').trim();
  if (explicit) {
    return explicit.endsWith('/api/v1') ? explicit : `${explicit.replace(/\/$/, '')}/api/v1`;
  }

  const serviceUrl = (process.env.RENDER_EXTERNAL_URL || '').trim();
  if (serviceUrl) {
    return `${serviceUrl.replace(/\/$/, '')}/api/v1`;
  }

  return '';
}

const apiBaseUrl = resolveApiBaseUrl();
if (!apiBaseUrl) {
  console.error('ERROR: Set API_BASE_URL or link okane-api RENDER_EXTERNAL_URL in render.yaml');
  process.exit(1);
}

const target = path.join(__dirname, '..', 'src', 'environments', 'environment.render.ts');
const content = `// Generated by scripts/render-set-api-url.js — do not edit on Render builds.
export const environment = {
  production: true,
  apiBaseUrl: '${apiBaseUrl.replace(/'/g, "\\'")}',
  useMockApi: false,
  allowMockFallback: false
};
`;

fs.writeFileSync(target, content, 'utf8');
console.log('Render API base URL:', apiBaseUrl);
